LDI R21,00 ;R21 = 00000000 in binary 

SBI DDRB,2
OUT PORTB,R21 ;PORTB = R20(on)
CALL Delay1 ;Delays for .435s
OUT PORTB,R20 ;PORTB = R21(off)
CALL Delay2 ;Delays for .290s
